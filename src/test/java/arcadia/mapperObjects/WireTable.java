package arcadia.mapperObjects;

public class WireTable {
    private String connectTo;
    private String wireDescription;

    public String getConnectTo() {
        return connectTo;
    }

    public void setConnectTo(String connectTo) {
        this.connectTo = connectTo;
    }

    public String getWireDescription() {
        return wireDescription;
    }

    public void setWireDescription(String wireDescription) {
        this.wireDescription = wireDescription;
    }
}
